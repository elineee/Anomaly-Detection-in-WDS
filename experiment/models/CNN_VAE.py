import numpy as np
from torch.utils.data import DataLoader
from data_transformation import remove_first_x_days
from experiment_config import ExperimentConfig, ContaminationType
from models.CNN import CNNModel
from models.VAE import VAEModel

class CNNVAEModel(CNNModel):
    def _call_second_model(self, node):
        """
        Calls the second model (a VAE Model) used to generate additional features for the CNN.
   
        Parameters:
        - node: the node id 
        
        Returns:
        - vae_model: an instantiated vae model 
        """
        
        if self.config.contaminants[0] == ContaminationType.ARSENIC:
            config_vae = ExperimentConfig(
                    config_name="VAE_arsenic",
                    contaminated_files=self.config.contaminated_files,
                    example_files=self.config.example_files,
                    nodes=[node],
                    window_size=100,
                    model_name="VAE",
                    model_params={},
                )
        else :
                
            config_vae = ExperimentConfig(
                    config_name="VAE",
                    contaminated_files=self.config.contaminated_files,
                    example_files=self.config.example_files,
                    nodes=[node],
                    window_size=288,
                    model_name="VAE",
                    model_params={},
                    contaminants=[ContaminationType.PATHOGEN]

                )
                
        vae_model = VAEModel(config_vae)
        return vae_model
      
  
    def _prepare_data(self, vae_model, df, clean_dfs, node):
        """ 
        Prepares data for training and testing the CNN model.
        
        Parameters:
        - vae_model: the vae model to use for generating features
        - df: the contaminated dataframe to use for training and testing
        - clean_dfs: a list of clean dataframes to use for training the vae model
        - node: the node id to use for generating features with the second model
        
        Returns:
        - prepared_df: the contaminated dataframe after removing the first 3 days
        - features: the features for training/testing the CNN model, where each feature is a sliding window of the time series data (shape (number of windows, window_size))
        - labels: the labels for training/testing the CNN model, where each label is a sliding window of the original labels (shape (number of windows, window_size))
        - y_vae: the features generated by the vae model, where each feature is a sliding window of the predicted values of the vae model (shape (number of windows, window_size))
        
        """
        X_train, X_test, _ = vae_model._prepare_data(clean_dfs, [df])
        train_batches = DataLoader(X_train, batch_size=32, shuffle=True)
        test_batches = DataLoader(X_test, batch_size=32, shuffle=False)
        _, _ , errors  = vae_model.run_model(train_batches, test_batches, epochs=300, hidden_dim=64, latent_dim=4, node = node)
            
        prepared_df = remove_first_x_days(df, 3)

        # Creating a signal point to point with errors values 
        signal_errors = np.zeros(len(prepared_df))
        vae_model_window_size = vae_model.config.window_size  
        for i, error in enumerate(errors):
            signal_errors[vae_model_window_size + i - 1] = error
        
        features, labels = self.create_labeled_features(prepared_df, self.config.disinfectant.value, self.config.contaminants[0].value, window_size=self.config.window_size)
        y_vae = self.create_direct_features(signal_errors, window_size=self.config.window_size)
        
        return prepared_df, features, labels, y_vae

